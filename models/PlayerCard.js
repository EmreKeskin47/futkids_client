class PlayerCard {
    constructor(
        id,
        playerID,
        name,
        position,
        overall,
        image,
        kitNumber,
        foot,
        age
    ) {
        this.id = id;
        this.playerID = playerID;
        this.name = name;
        this.position = position;
        this.overall = overall;
        this.image = image;
        this.kitNumber = kitNumber;
        this.foot = foot;
        this.age = age;
    }
}
export default PlayerCard;
