class PlayerDetails {
    constructor(
        playerID,
        phone,
        email,
        height,
        weight,
        playerCardId,
        attributesId,
        statisticsId
    ) {
        this.playerID = playerID;
        this.phone = phone;
        this.email = email;
        this.height = height;
        this.weight = weight;
        this.playerCardId = playerCardId;
        this.attributesId = attributesId;
        this.statisticsId = statisticsId;
    }
}
